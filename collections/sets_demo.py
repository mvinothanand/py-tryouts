# Set is a collection of 'unique' elements
# It is 'unordered' -> dont rely on the order of the elements

print(set())

print(set('superstar rajinikanth'))

print(set([1,2,1,2,3,4,5,6,0,9,12,22,3]))

print(set((1, 2, 3, 5, 5)))

print(set(set(['k', 'a', 'm', 'a', 'l', 'h', 'a', 'a', 's', 'a', 'n'])))

print(set({'name': 'rajinikanth', 'title': 'superstar'}))

print(set(enumerate(['r', 'a', 'j', 'i', 'n', 'i', 'k', 'a', 'n', 't', 'h'])))